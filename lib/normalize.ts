// Нормализация названия точки/магазина для сравнения: отбрасываем хвостовой
// телефон (6+ цифр в конце — в Excel-выгрузках он то актуальный, то старый, и не
// должен мешать сопоставлению), приводим к нижнему регистру. Общий хелпер —
// раньше эта же регулярка была скопирована в shops/page, lib/api и lib/deliveries.
export function normalizeName(name: unknown): string {
  return String(name ?? '').replace(/\s*\d{6,}\s*$/, '').trim().toLowerCase();
}
