// Нормализация названия точки/магазина для сравнения:
// — отбрасываем хвостовой телефон (6+ цифр в конце)
// — отбрасываем хвостовое имя оператора/контакта в скобках
//   (в Smartup-накладных to_name часто содержит «Склад X (Оператор)», а в
//   справочнике точка хранится просто как «Склад X» — должны совпадать)
// — приводим к нижнему регистру
// Общий хелпер — раньше эта же регулярка была скопирована в shops/page, lib/api и lib/deliveries.
export function normalizeName(name: unknown): string {
  return String(name ?? '')
    .replace(/\s*\d{6,}\s*$/, '')  // хвостовой телефон
    .replace(/\s*\([^)]+\)\s*$/, '') // хвостовой оператор в скобках
    .trim()
    .toLowerCase();
}
